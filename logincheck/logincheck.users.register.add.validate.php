/**
 * Мы регистрируем хук "users.register.add.validate" и используем для подключения нашего кода к файлу modules/users/inc/users.register.php в строке 100
 *  === Hook for the plugins === 
 *  foreach (cot_getextplugins('users.register.add.validate') as $pl) { // - это строка 100
 * 	  include $pl;
 *  }
 * ===== 
 * выполняемый на этапе валидации данных при регистрации пользователя,
 * Обеспечивает нормализацию и проверку логина перед сохранением в базу данных:
 *   - транслитерация нелатинских символов;
 *   - удаление недопустимых символов;
 *   - приведение к единому формату (латиница, цифры, дефис, подчёркивание);
 *   - проверка длины, первого символа и чёрного списка.
 */

defined('COT_CODE') or die('Wrong URL');

/* =========================================================================================
 * Подключение языкового файла плагина logincheck.
 * Файл содержит локализованные сообщения об ошибках валидации и другие текстовые строки,
 * используемые при проверке логина. Функция cot_langfile автоматически выбирает файл
 * в соответствии с текущей локалью системы.
 * ======================================================================================== */
require_once cot_langfile('logincheck', 'plug');

/* =========================================================================================
 * Извлечение имени пользователя из массива регистрационных данных.
 * Используется оператор объединения с null (??) для безопасной работы с неопределённым ключом.
 * ======================================================================================== */
$username = $ruser['user_name'] ?? '';

/* =========================================================================================
 * Основной блок нормализации логина выполняется только если передано непустое значение.
 * Пустой логин будет отклонён на более поздних этапах стандартной валидацией Cotonti.
 * ======================================================================================== */
if ($username !== '') {

    /* -------------------------------------------------------------------------------------
     * Транслитерация строки с использованием встроенного механизма Cotonti.
     * Преобразует кириллические и другие нелатинские символы в латиницу на основе
     * активной таблицы транслитерации (настраивается в языковых файлах).
     * ------------------------------------------------------------------------------------- */
    $username = cot_translit_encode($username);

    /* -------------------------------------------------------------------------------------
     * Замена любых пробельных символов (пробелы, табуляции, переводы строк) на подчёркивание.
     * Модификатор 'u' обеспечивает корректную работу с UTF-8.
     * ------------------------------------------------------------------------------------- */
    $username = preg_replace('/\s+/u', '_', $username);

    /* -------------------------------------------------------------------------------------
     * Удаление всех символов, кроме латинских букв (строчных и заглавных), цифр,
     * подчёркивания и дефиса. Замена недопустимых символов на дефис.
     * ------------------------------------------------------------------------------------- */
    $username = preg_replace('/[^a-zA-Z0-9_-]/', '-', $username);

    /* -------------------------------------------------------------------------------------
     * Сжатие нескольких идущих подряд дефисов в один.
     * Предотвращает появление логинов вида "user----name".
     * ------------------------------------------------------------------------------------- */
    $username = preg_replace('/-+/', '-', $username);

    /* -------------------------------------------------------------------------------------
     * Сжатие нескольких идущих подряд подчёркиваний в одно.
     * Аналогично предыдущему шагу, но для символа подчёркивания.
     * ------------------------------------------------------------------------------------- */
    $username = preg_replace('/_+/', '_', $username);

    /* -------------------------------------------------------------------------------------
     * Удаление дефисов и подчёркиваний в начале и конце строки.
     * Гарантирует, что логин не начинается и не заканчивается разделительным символом,
     * что соответствует общепринятым правилам имён пользователей.
     * ------------------------------------------------------------------------------------- */
    $username = trim($username, '-_');

    /* -------------------------------------------------------------------------------------
     * Если после всех преобразований строка стала пустой, регистрация прерывается.
     * Пользователь получает сообщение об ошибке — в логине недопустимые символы.
     * ------------------------------------------------------------------------------------- */
    if ($username === '') {
        cot_error($L['logincheck_error_invalidchars'], 'rusername');
        return;
    }

    /* -------------------------------------------------------------------------------------
     * Ограничение длины логина до 32 символов.
     * Соответствует стандартному ограничению поля user_name в базе данных Cotonti
     * (тип VARCHAR(32)) и предотвращает проблемы с отображением в интерфейсе.
     * ------------------------------------------------------------------------------------- */
    $username = mb_substr($username, 0, 32);

    /* -------------------------------------------------------------------------------------
     * Проверка первого символа: если логин не начинается с латинской буквы,
     * к нему добавляется префикс 'user_'. Это гарантирует соответствие регулярному выражению
     * финальной проверки и требованиям системы к формату имени пользователя.
     * ------------------------------------------------------------------------------------- */
    if (!preg_match('/^[a-zA-Z]/', $username)) {
        $username = 'user_' . $username;
    }

    /* -------------------------------------------------------------------------------------
     * Сохранение обработанного и нормализованного логина обратно в массив регистрационных
     * данных. Далее Cotonti использует это значение при создании учётной записи.
     * ------------------------------------------------------------------------------------- */
    $ruser['user_name'] = $username;
}

/* =========================================================================================
 * Финальная проверка логина строгим регулярным выражением.
 * Требования:
 *   - первый символ — латинская буква (строчная или заглавная);
 *   - остальные символы — латинские буквы, цифры, подчёркивание или дефис;
 *   - длина не менее одного символа (уже проверено непустым значением).
 * Если проверка не пройдена — возвращается ошибка.
 * ======================================================================================== */
if (!empty($ruser['user_name']) &&
    !preg_match("/^[a-zA-Z][_a-zA-Z0-9-]*$/", $ruser['user_name'])) {
    cot_error($L['logincheck_error_invalidchars'], 'rusername');
}

/* =========================================================================================
 * Проверка логина на совпадение с запрещёнными именами из конфигурации плагина.
 * Список запрещённых имён хранится в поле конфигурации плагина настройке `plugin.logincheck.invalidnames`
 * (строка с разделителем-запятой). Если логин найден в чёрном списке — регистрация
 * отклоняется с соответствующим сообщением.
 * ======================================================================================== */
if (!empty($cfg['plugin']['logincheck']['invalidnames'])) {

    /* -------------------------------------------------------------------------------------
     * Преобразование строки с запрещёнными именами в массив.
     * Каждое значение очищается от лишних пробелов функцией trim.
     * ------------------------------------------------------------------------------------- */
    $invalidnames = array_map('trim', explode(',', $cfg['plugin']['logincheck']['invalidnames']));

    /* -------------------------------------------------------------------------------------
     * Проверка вхождения логина в массив запрещённых имён.
     * Используется строгое сравнение (третий параметр in_array = true).
     * ------------------------------------------------------------------------------------- */
    if (in_array($ruser['user_name'], $invalidnames, true)) {
        cot_error($L['logincheck_error_invalidname'], 'rusername');
    }
}